/*
   Access Modifiers in Java:

   1. public    → Accessible from anywhere in the program.
   2. protected → Accessible within the same package and by subclasses.
   3. default  → (no modifier) Accessible only within the same package.
   4. private  → Accessible only within the same class.

   → They help in controlling the visibility and security of classes,
     methods, and variables.
*/


/*
   Access Modifiers in Java

   +------------+--------------------------------------------+---------------------------+
   | Modifier   | Accessibility                              | Usage Example             |
   +------------+--------------------------------------------+---------------------------+
   | public     | Accessible from anywhere                   | public class A            |
   | protected  | Same package + subclasses (even outside)    | protected void show()      |
   | default    | (no keyword) Same package only             | void display()            |
   | private    | Only within the same class                 | private int data;         |
   +------------+--------------------------------------------+---------------------------+

   → Access modifiers help in data hiding, encapsulation, and security.
*/
